var first = prompt("عدد اول را وارد کنید :" , "مثال : 0")
var firstNum = +first
var sec = prompt("عدد دوم را وارد کنید :", "مثال : 2")
var secNum = +sec

alert("جمع اعداد برابر هست با " + (firstNum + secNum))
alert("تفریق اعداد برابر هست با " + (firstNum - secNum))
alert("ضرب اعداد برابر هست با " + (firstNum * secNum))
alert("تقسیم اعداد برابر هست با " + (firstNum / secNum))